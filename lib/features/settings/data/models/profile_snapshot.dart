class ProfileSnapshot {
  const ProfileSnapshot({
    required this.firstName,
    required this.lastName,
    required this.className,
    required this.voice,
    required this.role,
    required this.choir,
    required this.diet,
  });

  final String? firstName;
  final String? lastName;
  final String? className;
  final String? voice;
  final String? role;
  final String? choir;
  final String? diet;
}
